package Utils;

public class offerToOrder {

    private int idoffer;
    private String nameofOffer;
    private double quantityOffer;
    private double priceOffer;

    public int getIdoffer() {
        return idoffer;
    }

    public void setIdoffer(int idoffer) {
        this.idoffer = idoffer;
    }

    public String getNameofOffer() {
        return nameofOffer;
    }

    public void setNameofOffer(String nameofOffer) {
        this.nameofOffer = nameofOffer;
    }

    public double getQuantityOffer() {
        return quantityOffer;
    }

    public void setQuantityOffer(double quantityOffer) {
        this.quantityOffer = quantityOffer;
    }

    public double getPriceOffer() {
        return priceOffer;
    }

    public void setPriceOffer(double priceOffer) {
        this.priceOffer = priceOffer;
    }
}
