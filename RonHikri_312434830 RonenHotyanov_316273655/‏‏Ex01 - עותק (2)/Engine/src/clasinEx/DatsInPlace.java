package clasinEx;

public class DatsInPlace {

    private String nameStoreManager;
    private String szone;
    private Integer sumSortsItems;
    private Integer SumStores;
    private Integer SumOrders;
    private Double AvgPriceOrder;

    public String getNameStoreManager() {
        return nameStoreManager;
    }

    public void setNameStoreManager(String nameStoreManager) {
        this.nameStoreManager = nameStoreManager;
    }

    public String getSzone() {
        return szone;
    }

    public void setSzone(String szone) {
        this.szone = szone;
    }

    public Integer getSumSortsItems() {
        return sumSortsItems;
    }

    public void setSumSortsItems(Integer sumSortsItems) {
        this.sumSortsItems = sumSortsItems;
    }

    public Integer getSumStores() {
        return SumStores;
    }

    public void setSumStores(Integer sumStores) {
        SumStores = sumStores;
    }

    public Integer getSumOrders() {
        return SumOrders;
    }

    public void setSumOrders(Integer sumOrders) {
        SumOrders = sumOrders;
    }

    public Double getAvgPriceOrder() {
        return AvgPriceOrder;
    }

    public void setAvgPriceOrder(Double avgPriceOrder) {
        AvgPriceOrder = avgPriceOrder;
    }
}
