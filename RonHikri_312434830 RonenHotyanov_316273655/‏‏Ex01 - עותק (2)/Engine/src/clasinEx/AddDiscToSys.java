package clasinEx;

public class AddDiscToSys {

    private String nameDisc;
    private int idOfStoreOfItem;
    private int idItem;
    private double Amount;
    private String Operator;


    public String getNameDisc() {
        return nameDisc;
    }

    public void setNameDisc(String nameDisc) {
        this.nameDisc = nameDisc;
    }

    public int getIdOfStoreOfItem() {
        return idOfStoreOfItem;
    }

    public void setIdOfStoreOfItem(int idOfStoreOfItem) {
        this.idOfStoreOfItem = idOfStoreOfItem;
    }

    public int getIdItem() {
        return idItem;
    }

    public void setIdItem(int idItem) {
        this.idItem = idItem;
    }

    public double getAmount() {
        return Amount;
    }

    public void setAmount(double amount) {
        Amount = amount;
    }

    public String getOperator() {
        return Operator;
    }

    public void setOperator(String operator) {
        Operator = operator;
    }
}
