package clasinEx;

public class ShowStores {

    private Integer id;
    private String name;
    private Integer ppk;
    private Double sumsend;

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public Integer getPpk() {
        return ppk;
    }

    public void setPpk(Integer ppk) {
        this.ppk = ppk;
    }

    public Double getSumsend() {
        return sumsend;
    }

    public void setSumsend(Double sumsend) {
        this.sumsend = sumsend;
    }
}
